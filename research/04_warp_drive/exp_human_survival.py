alpha_s_now = 0.1179
alpha_s_new = alpha_s_now / 1.32  # 1/α increases by 32%, so α decreases

print(f"  α_s (現在) = {alpha_s_now}")
print(f"  α_s (ミュート後) = {alpha_s_new:.4f}")
print(f"  変化率: {(alpha_s_new/alpha_s_now - 1)*100:+.1f}%")
