# QCD confinement scale: Λ_QCD ∝ μ × exp(-2π/(b₀ α_s(μ)))
# At μ = M_Z = 91.2 GeV, b₀ = 7 (N_f=6 at that scale):

b0 = 7
MZ = 91.2  # GeV

Lambda_now = MZ * np.exp(-2*pi/(b0 * alpha_s_now))
Lambda_new = MZ * np.exp(-2*pi/(b0 * alpha_s_new))

print(f"  Λ_QCD (現在): M_Z × exp(-2π/(7×{alpha_s_now})) = {Lambda_now*1000:.1f} MeV")
print(f"  Λ_QCD (ミュート後): M_Z × exp(-2π/(7×{alpha_s_new:.4f})) = {Lambda_new*1000:.1f} MeV")
print(f"  比: {Lambda_new/Lambda_now:.4f}")
print()

# The proton mass scales roughly as Λ_QCD
mp_now = 938  # MeV
mp_new = mp_now * Lambda_new / Lambda_now

print(f"  陽子質量の変化:")
print(f"    現在: {mp_now} MeV")
print(f"    ミュート後: {mp_new:.0f} MeV (Λ_QCD に比例として)")
