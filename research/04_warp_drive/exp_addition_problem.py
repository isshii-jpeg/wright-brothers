# 2j/|B_{2j}| for j = 1, ..., 10
B = {2: 1/6, 4: -1/30, 6: 1/42, 8: -1/30, 10: 5/66, 12: -691/2730}
values = []
for j in range(1, 7):
    b2j = B.get(2*j)
    if b2j is not None:
        val = 2*j / abs(b2j)
        values.append((j, val))
        print(f"  j={j}: 2j/|B_{{2j}}| = {val:.1f}")

print()

# How many ways to pick a SUBSET of these and sum to near 137?
alpha_inv = 137.036
print(f"  目標: 1/α = {alpha_inv:.3f}")
print()

# Check all subsets
from itertools import combinations

close_matches = []
for r in range(1, len(values)+1):
    for combo in combinations(values, r):
        total = sum(v for _, v in combo)
        if abs(total - alpha_inv) < 10:  # within 10
            js = [j for j, _ in combo]
            close_matches.append((js, total, abs(total - alpha_inv)))

close_matches.sort(key=lambda x: x[2])
print(f"  |和 - 137| < 10 の部分集合:")
print(f"  {'j の組':>20s} {'和':>10s} {'|差|':>10s}")
print(f"  {'-'*44}")
for js, total, diff in close_matches[:10]:
    print(f"  {str(js):>20s} {total:>10.1f} {diff:>10.1f}")

print()

# How many subsets total?
n_total = 2**len(values) - 1  # exclude empty set
print(f"  全部分集合数（空集合除く）: {n_total}")
print(f"  |差| < 10 の個数: {len(close_matches)}")
print(f"  |差| < 5 の個数: {sum(1 for _,_,d in close_matches if d < 5)}")
print(f"  |差| < 1 の個数: {sum(1 for _,_,d in close_matches if d < 1)}")
print()

# The j={1,2} combination gives 132, |diff| = 5.0.
