# The KEY relation: for ODD K groups,
# |K_{4n-1}(Z)_tors| = numerator of |B_{2n}/(4n)| × (power of 2)

# K₃: |48| and B₂/4 = 1/24. 48 = 2 × 24. Factor of 2.
# K₇: |240| and B₄/8 = -1/240. 240 = 1 × 240. Exact!
