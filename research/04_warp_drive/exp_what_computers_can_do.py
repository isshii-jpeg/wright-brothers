# Actually the Quillen-Lichtenbaum for Z:
# |K_{2n}(Z)_tors| / |K_{2n+1}(Z)_tors| = |B_{n+1}|/(n+1) × (2-power)

# For K_{2n}(Z[√2]): these would involve ζ_{Q(√2)}(1-n) = ζ(1-n) × L(χ₈, 1-n)

print(f"  ── Q(√2) のゼータ特殊値 ──")
