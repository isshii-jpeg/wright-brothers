for n in range(1, 6):
    zeta_neg = float(mpmath.zeta(1-2*n))
    # L(χ₈, 1-2n): generalized Bernoulli numbers
    # For simplicity, compute numerically
    def L_chi8(s, N=100000):
        total = 0.0
        for m in range(1, N+1):
            if m % 2 == 0: continue
            chi = 1 if m % 8 in [1, 7] else -1
            total += chi * m**(-s)
        return total

    if 1-2*n < 0:
        L_val = L_chi8(1-2*n, 50000) if n <= 3 else 0
    else:
        L_val = 0

    zeta_K = zeta_neg * L_val if L_val != 0 else 0

    print(f"  n={n}: ζ({1-2*n}) = {zeta_neg:.6f}, "
          f"L(χ₈,{1-2*n}) ≈ {L_val:.6f}, "
          f"ζ_K({1-2*n}) = {zeta_K:.6f}")

