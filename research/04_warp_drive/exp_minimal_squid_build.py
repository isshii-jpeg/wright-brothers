import numpy as np

pi = np.pi
hbar = 1.054571817e-34
c = 2.99792458e8
k_B = 1.380649e-23
Phi_0 = 2.067833848e-15
e_charge = 1.602176634e-19

print("=" * 70)
print("  MINIMAL SQUID EXPERIMENT: DETAILED BUILD GUIDE")
print("=" * 70)

# ============================================================================
#  PART 1: 3D Aluminum Cavity
# ============================================================================

print("""
  ■ PART 1: 3Dアルミキャビティ

  ── なぜ3Dキャビティか ──

  CPW共振器（2D）: Nb薄膜が必要 → スパッタリング装置 → 高価
  3Dキャビティ: アルミブロックに穴を開けるだけ → 町工場で製作可能

  Yale大学のPaik et al. (2011)が実証:
  - 6061アルミ合金ブロック → 機械加工 → Q > 10⁷
  - 2Dの100倍以上のQ値
  - 製作コスト: ~$500 (機械加工代)

  ── 設計 ──

  形状: 矩形キャビティ（直方体の空洞）
  サイズ: 内寸 26mm × 26mm × 10mm

  基本モード（TE₁₀₁）:
""")

# Cavity dimensions
a = 26e-3  # width [m]
b = 10e-3  # height [m]
d = 26e-3  # length [m]

# TE_mnp mode frequencies
def cavity_freq(m, n, p, a, b, d):
    """Resonant frequency of rectangular cavity mode TE_mnp."""
    return c / 2 * np.sqrt((m/a)**2 + (n/b)**2 + (p/d)**2)

f_101 = cavity_freq(1, 0, 1, a, b, d)
f_201 = cavity_freq(2, 0, 1, a, b, d)
f_102 = cavity_freq(1, 0, 2, a, b, d)

print(f"  キャビティ内寸: {a*1e3:.0f} × {b*1e3:.0f} × {d*1e3:.0f} mm")
print()
print(f"  モードスペクトル:")
print(f"    TE₁₀₁ (基本): f₁ = {f_101/1e9:.3f} GHz")
print(f"    TE₂₀₁:         f₂ = {f_201/1e9:.3f} GHz")
print(f"    TE₁₀₂:         f₃ = {f_102/1e9:.3f} GHz")
print()

# For our experiment: we want f₁ ~ 6 GHz
# f₁ = c/(2a) × √2 for a = d (square cavity)
# a = c√2 / (2f₁) = 3e8 × 1.414 / (2 × 6e9) = 35.4 mm... too big for 6 GHz
# For 6 GHz: a = c/(2f × √(1/a² + 1/d²))

# Let's target f₁ ~ 8 GHz (more practical for this cavity size)
# Or adjust cavity to get ~6 GHz

a_target = c * np.sqrt(2) / (2 * 6e9)
print(f"  f₁ = 6 GHz に必要な正方キャビティのサイズ:")
print(f"    a = d = c√2/(2f₁) = {a_target*1e3:.1f} mm")
print()

# Let's use a = d = 35 mm, b = 10 mm
a2 = 35e-3
d2 = 35e-3
b2 = 10e-3
f1_new = cavity_freq(1, 0, 1, a2, b2, d2)
print(f"  採用: a = d = 35 mm, b = 10 mm")
print(f"    f₁(TE₁₀₁) = {f1_new/1e9:.3f} GHz ✓")
print()

# List first several modes
print(f"  最初の10モード:")
modes = []
for m in range(0, 5):
    for n in range(0, 3):
        for p in range(0, 5):
            if m == 0 and n == 0:
                continue
            if m == 0 and p == 0:
                continue
            f = cavity_freq(m, n, p, a2, b2, d2)
            if f < 30e9:
                modes.append((m, n, p, f))

modes.sort(key=lambda x: x[3])

print(f"  {'#':>4s}  {'Mode':>10s}  {'f [GHz]':>10s}  {'f/f₁':>8s}  {'偶数?':>6s}")
print(f"  {'-'*42}")
for i, (m, n, p, f) in enumerate(modes[:12]):
    ratio = f / modes[0][3]
    is_even = "★" if round(ratio) % 2 == 0 and abs(ratio - round(ratio)) < 0.1 else ""
    print(f"  {i+1:>4d}  TE_{m}{n}{p}  {f/1e9:>10.3f}  {ratio:>8.2f}  {is_even:>6s}")

print()

print("""
  ── 製作手順 ──

  1. 材料: 6061-T6 アルミ合金ブロック
     サイズ: 50 × 50 × 30 mm（外形、肉厚 ~7.5mm）
     入手: MonotaRO、Amazon、金属材料商
     コスト: ~500円

  2. 機械加工: CNCフライスまたは汎用フライス
     内部に 35 × 35 × 10 mm の空洞を掘る
     上面を蓋（ねじ止め）にする → 2パーツ構成
     入出力ポート: φ3mm の穴を2箇所（SMAコネクタ用）

     町工場外注: ~3万〜5万円
     大学の工作室: ~5千円（材料費のみ）
     自分でやる（卓上フライス盤）: ~5千円

  3. 表面処理:
     内面を酸洗い（リン酸 or クエン酸）→ 自然酸化膜除去
     超音波洗浄（アセトン → IPA → 純水）
     → 表面のAl₂O₃が自然に再形成（これが高Qの鍵）

  4. SMAコネクタの取付:
     SMA-KFD (フランジマウント) × 2個: ~2000円
     中心ピンがキャビティ内に ~3mm 突出 → 結合アンテナ

  5. 組み立て:
     上蓋をインジウム (In) ワイヤーガスケットで密封
     In ワイヤー (φ1mm): ~1000円/m
     ネジ: M3 × 8本

  合計コスト: ~5万〜8万円
""")

