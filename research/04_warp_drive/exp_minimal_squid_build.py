# Al Tc = 1.2 K
Tc_Al = 1.2  # K
T_target = 0.3  # 3He fridge base temp

# Thermal photon number at 300 mK for f₁ ~ 6 GHz
f1_val = f1_new
n_th_300 = 1 / (np.exp(hbar * 2 * pi * f1_val / (k_B * T_target)) - 1)
n_th_20 = 1 / (np.exp(hbar * 2 * pi * f1_val / (k_B * 0.02)) - 1)

print(f"  Al の超伝導転移温度: Tc = {Tc_Al} K")
print(f"  3He冷凍機のベース温度: T = {T_target*1e3:.0f} mK")
print(f"  T < Tc ✓ (Alは超伝導状態)")
print()
print(f"  熱的光子数（f₁ = {f1_val/1e9:.2f} GHz）:")
print(f"    T = 300 mK (3He): ⟨n_th⟩ = {n_th_300:.4f}")
print(f"    T = 20 mK (希釈): ⟨n_th⟩ = {n_th_20:.2e}")
print()

# SNR degradation at 300 mK
snr_factor = 1 / (1 + 2 * n_th_300)
print(f"  300 mK でのSNR劣化: ×{snr_factor:.3f} ({snr_factor*100:.1f}%)")
print(f"  → 積分時間を {1/snr_factor**2:.1f}倍にすればSNR回復")
print(f"  → 20mK で36秒 → 300mKで ~{36/snr_factor**2:.0f}秒 = ~{36/snr_factor**2/60:.0f}分")

