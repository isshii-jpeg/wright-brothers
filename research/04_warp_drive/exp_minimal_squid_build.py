print("""
  SQUIDをキャビティに結合する方法:

  (A) 磁気結合:
      SQUIDループをキャビティの磁場最大点に配置。
      ループが磁束を拾い、共振周波数が変化。
      3Dキャビティの壁の内面にSQUIDチップをマウント。

  (B) 電気結合:
      SQUIDのシャント容量をキャビティの電場最大点に配置。
      アンテナパッドを介して結合。

  推奨: (A) 磁気結合（シンプルで制御しやすい）

  ── 具体的な実装 ──

  1. SQUIDチップ（Si基板上、~2mm × 2mm）を製作
  2. キャビティ底面の磁場最大点にインジウムはんだでマウント
  3. 磁束バイアス線: 細いNbTi線をキャビティに通す
     または外部コイル（キャビティの外側に巻く）

  外部コイルの方がシンプル:
  - 銅線（φ0.1mm）をキャビティの外周に10回巻く
  - DC電流 I_bias で磁束を制御
  - 必要電流: I_bias = Φ_ext / (M × N_turns)
  - M (相互インダクタンス) ~ 1 pH/turn
  - Φ_ext ~ 0.5 Φ₀ = 1.03 × 10⁻¹⁵ Wb
  - I_bias ~ 0.5 Φ₀ / (10 × 1e-12) ~ 0.1 mA
  → 普通のDC電源で十分
""")

