# Compute expected signal
g = 100e6  # coupling, Hz
f_q = 5e9  # qubit frequency

print(f"  ── 予測シグナル ──")
print()

lamb_all = 0  # Lamb shift from all modes
lamb_odd = 0  # Lamb shift from odd modes only

for n in range(1, 9):
    f_n = n * f1
    delta = f_q - f_n
    if abs(delta) < 1e6:
        continue  # skip near-resonance
    g_n = g * np.sqrt(f1/f_n)  # mode-dependent coupling (approximate)
    shift = g_n**2 / delta
    lamb_all += shift
    if n % 2 == 1:
        lamb_odd += shift

delta_lamb = lamb_odd - lamb_all  # the change when even modes are muted

print(f"  Lamb シフト（全モード）: {lamb_all/1e3:.2f} kHz")
print(f"  Lamb シフト（奇数のみ）: {lamb_odd/1e3:.2f} kHz")
print(f"  差: {delta_lamb/1e3:.2f} kHz = {abs(delta_lamb):.0f} Hz")
print()

# This is the signal: the DIFFERENCE in Lamb shift when even modes are muted.
# It equals the sum of even-mode contributions (which are removed).

lamb_even = lamb_all - lamb_odd
print(f"  偶数モードの寄与（= 除去される量）: {lamb_even/1e3:.2f} kHz")
print(f"  ノイズフロア（1hr積分）: ~1 Hz")
print(f"  S/N: {abs(lamb_even):.0f}")
print()

# Sign?
