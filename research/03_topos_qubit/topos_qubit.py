# ============================================================================
#  PART 3: Daseinisation of Projections
# ============================================================================
#
# A quantum proposition "spin-z is up" corresponds to the projection:
#   P_z_up = |↑⟩⟨↑| = (1/2)(I + sigma_z) = [[1,0],[0,0]]
#
# Daseinisation approximates this projection in each context:
#
#   delta(P_z_up)(V_n) = "smallest projection in V_n that is >= P_z_up"
#                      = the "outer daseinisation"
#
# For V_z:  delta(P)(V_z) = P_z_up itself (P is already in V_z)
# For V_0:  delta(P)(V_0) = I (only projection in V_0 >= P is identity)
# For V_n (n != z): delta(P)(V_n) depends on angle between n and z:
#   If n.z > 0: delta(P)(V_n) = P_n_up = (1/2)(I + n.sigma)
#   If n.z < 0: delta(P)(V_n) = I (no proper projection >= P)
#   If n.z = 0: delta(P)(V_n) = I (e.g., V_x: spin-x doesn't dominate spin-z)

print("\n--- PART 3: Daseinisation ---")
print()
print("  Proposition: 'spin-z is up'")
print("  Projection:  P = |up><up| = (I + sigma_z)/2")
print()

# Concrete computation
sigma_z = np.array([[1, 0], [0, -1]], dtype=complex)
sigma_x = np.array([[0, 1], [1, 0]], dtype=complex)
sigma_y = np.array([[0, -1j], [1j, 0]], dtype=complex)
I2 = np.eye(2, dtype=complex)

P_z_up = (I2 + sigma_z) / 2  # |↑⟩⟨↑|

def projection_in_direction(n):
    """Projection onto spin-up along direction n = (nx, ny, nz)."""
    nx, ny, nz = n
    return (I2 + nx * sigma_x + ny * sigma_y + nz * sigma_z) / 2

def daseinise(P, n):
    """
    Outer daseinisation of projection P in context V_n.
    Returns the smallest projection in V_n that is >= P.

    In V_n, the projections are: 0, P_n_up, P_n_down, I.
    We need the smallest among {P_n_up, P_n_down, I} that >= P.
    Q >= P iff P*Q = P (equivalently, range(P) ⊆ range(Q)).
    """
    P_n_up = projection_in_direction(n)
    P_n_down = I2 - P_n_up

    # Check P_n_up >= P
    diff_up = P_n_up - P
    eigenvalues_up = np.linalg.eigvalsh(diff_up)
    up_dominates = np.all(eigenvalues_up >= -1e-10)

    # Check P_n_down >= P
    diff_down = P_n_down - P
    eigenvalues_down = np.linalg.eigvalsh(diff_down)
    down_dominates = np.all(eigenvalues_down >= -1e-10)

    if up_dominates and down_dominates:
        # Both dominate; return the smaller one (lower trace)
        if np.trace(P_n_up).real <= np.trace(P_n_down).real:
            return P_n_up, "P_n_up"
        else:
            return P_n_down, "P_n_down"
    elif up_dominates:
        return P_n_up, "P_n_up"
    elif down_dominates:
        return P_n_down, "P_n_down"
    else:
        return I2, "I"

# Test daseinisation for various directions
directions = {
    "z  = (0,0,1) ": (0, 0, 1),
    "-z = (0,0,-1)": (0, 0, -1),
    "x  = (1,0,0) ": (1, 0, 0),
    "y  = (0,1,0) ": (0, 1, 0),
    "45 = (0,s,s) ": (0, 1/np.sqrt(2), 1/np.sqrt(2)),
    "135= (0,s,-s)": (0, 1/np.sqrt(2), -1/np.sqrt(2)),
}

print("  Daseinisation of P_z_up in each context V_n:")
print("  " + "-" * 55)
for name, n in directions.items():
    _, label = daseinise(P_z_up, n)
    angle = np.arccos(np.clip(n[2], -1, 1)) * 180 / np.pi
    print(f"    V_{name}: delta(P)(V_n) = {label:8s}  (angle from z: {angle:.0f} deg)")

print()
print("  Interpretation:")
print("    - In V_z: P_z_up is EXACTLY the proposition (no approximation)")
print("    - In V_45: P_45_up dominates P_z_up (45 deg cone contains z-up)")
print("    - In V_x: neither P_x_up nor P_x_down dominates P_z_up")
print("      => daseinisation falls back to I (trivially true)")
print("    - In V_{-z}: P_{-z}_down = P_z_up, so it works")
print()
print("  THIS IS CONTEXTUALITY made mathematically precise:")
print("  The 'truth' of 'spin-z is up' depends on which context you're in.")

# ============================================================================
#  PART 4: Truth Values in the Heyting Algebra
# ============================================================================
#
# In the topos Set^{V(N)^op}, the subobject classifier Omega assigns
# to each context V the set of "sieves" on V:
#
#   Omega(V) = {S ⊆ ↓V : S is a sieve}
#
# where ↓V = {W ∈ V(N) : W ⊆ V}.
#
# For our fan-shaped poset:
#   ↓V_0 = {V_0}
#   ↓V_n = {V_0, V_n}
#
# Sieves on V_n: subsets S of {V_0, V_n} that are downward-closed:
#   S = {}        (empty sieve → "totally false")
#   S = {V_0}     (partial truth: true at V_0 but not V_n)
#   S = {V_0, V_n} (maximal sieve → "totally true")
#
# Wait - {V_0} is not a sieve on V_n because it's not downward-closed
# with respect to the arrow V_0 → V_n. Actually, sieves on V in the
# category V(N)^op consist of all W ≥ V such that... Let me be precise.
#
# A sieve on V in V(N)^op is a set S of morphisms TO V (in V(N)^op),
# i.e., inclusions V ⊆ W in V(N), that is closed under precomposition.
#
# For V_n: arrows to V_n in V(N)^op = inclusions V_n ⊆ W in V(N).
# Since V_n is maximal, the only arrow is id: V_n → V_n.
# So Omega(V_n) = {empty, {id}} = {0, 1} ≅ {false, true}.
#
# For V_0: arrows to V_0 in V(N)^op = inclusions V_0 ⊆ W in V(N).
# These are: V_0 ⊆ V_0, V_0 ⊆ V_x, V_0 ⊆ V_y, V_0 ⊆ V_z, ...
# A sieve S on V_0 is a subset closed under precomposition.
# For our fan: a sieve on V_0 is determined by which V_n's it contains.
#
# For a finite set of contexts {V_0, V_x, V_y, V_z}:
# Omega(V_0) has elements corresponding to subsets of {V_x, V_y, V_z} plus V_0.

