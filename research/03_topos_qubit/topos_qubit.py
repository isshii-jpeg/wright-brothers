# ============================================================================
#  PART 2: The Spectral Presheaf Sigma
# ============================================================================
#
# The spectral presheaf assigns to each context V its Gelfand spectrum:
#
#   Sigma(V_0) = {*}       (one point - the trivial character)
#   Sigma(V_n) = {λ_+, λ_-}  (two points - the two eigenvalues ±1)
#
# The restriction maps (for V_0 ⊂ V_n):
#   Sigma(V_n) → Sigma(V_0):  both λ_+ and λ_- map to *
#
# So the spectral presheaf looks like:
#
#   {+,-}  {+,-}  {+,-}  {+,-}   <- Sigma(V_n) = two points each
#     \\      |      /      /
#      \\     |     /      /      <- all collapse to single point
#       \\    |    /      /
#          {*}                    <- Sigma(V_0) = one point

