# The truth value is computed by:
# For each context V_n, check if the state |↑⟩ "makes the daseinised
# proposition true" at V_n.
#
# At V_n: the daseinised proposition delta(P)(V_n) is a clopen subset
# of Sigma(V_n). The state |↑⟩ defines a "pseudo-state" (not a global
# section of Sigma, but a natural transformation).
#
# For a pure state |ψ⟩, the truth value at context V_n is:
#   ⟨ψ| delta(P)(V_n) |ψ⟩ = 1  →  V_n is in the truth value sieve
#   ⟨ψ| delta(P)(V_n) |ψ⟩ < 1  →  V_n is NOT in the sieve

psi = np.array([1, 0], dtype=complex)  # |↑⟩

print("  Truth value sieve (which contexts make the proposition true):")
print("  " + "-" * 50)
truth_sieve = []
for name, n in directions.items():
    D, label = daseinise(P_z_up, n)
    expectation = np.real(psi.conj() @ D @ psi)
    in_sieve = np.isclose(expectation, 1.0)
    truth_sieve.append((name, in_sieve))
    status = "IN SIEVE (true)" if in_sieve else "not in sieve"
    print(f"    V_{name}: <up|delta(P)(V)|up> = {expectation:.3f}  -> {status}")

print()
print("  Result: The truth value of 'spin-z is up' in state |up> is")
