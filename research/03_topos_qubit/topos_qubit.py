# ============================================================================
#  PART 1: The Context Category V(M_2(C))
# ============================================================================
#
# The algebra of observables for a qubit is M_2(C) (2x2 complex matrices).
# A "context" V is a commutative *-subalgebra of M_2(C).
#
# For M_2(C), the contexts are:
#   - The trivial subalgebra: V_0 = C*I (scalar multiples of identity)
#   - For each unit vector n = (nx,ny,nz) on the Bloch sphere:
#     V_n = {a*I + b*(n.sigma) : a,b in C}
#     where n.sigma = nx*sigma_x + ny*sigma_y + nz*sigma_z
#
# V_n is the algebra generated by the spin observable in direction n.
# It is isomorphic to C^2 (two eigenvalues).
#
# The poset structure: V_0 ⊂ V_n for all n.
# No V_n is contained in another V_m (for n ≠ m).
#
# So V(M_2(C)) looks like a "fan":
#
#       V_z    V_x    V_y    V_{...}
#        \      |      /      /
#         \     |     /      /
#          \    |    /      /
#           \   |   /      /
#             V_0
#              (bottom)

