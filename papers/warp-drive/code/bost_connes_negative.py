def bernoulli(n):
    B = [Fraction(0)] * (n + 1)
    B[0] = Fraction(1)
    if n >= 1: B[1] = Fraction(-1, 2)
    for m in range(2, n + 1):
        if m % 2 == 1 and m > 1:
            B[m] = Fraction(0)
            continue
        s = Fraction(0)
        for j in range(m):
            binom = 1
            for i in range(j):
                binom = binom * (m + 1 - i) // (i + 1)
            s += binom * B[j]
        B[m] = -s / (m + 1)
    return B[n]

